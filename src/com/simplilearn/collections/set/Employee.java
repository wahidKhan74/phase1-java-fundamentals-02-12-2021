package com.simplilearn.collections.set;

public class Employee {

	int id;
	String name;
	double salary;
	String dept;
	
	public Employee() {} 
	
	public Employee(int id, String name, double salary, String dept) {
		this.id = id;
		this.name = name;
		this.salary = salary;
		this.dept= dept;
	}
	
}
